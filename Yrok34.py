"""
Цей код запитує користувача ввести поточний рік та їх вік.
Згідно введених даних, програма обчислює рік народження і виводить повідомлення
з приблизним роком народження користувача.
"""
a = int(input("Введіть скільки вам лет: "))
h = int(input("Введіть якій сейчас рік: "))
h += 1
a += 1
f = h - a
e = h - a
for i in range(a):
    a = f + i
    print(f"Тобі в",{a},"рокі було",{i},"років")
print(f"Ти родился в",{e},"рокі")