# Вкладений if
# 2. Задано кількість годин та хвилин. Використовуючи вкладений умовний оператор,
# перевірити, чи введені години та хвилини є допустимими значеннями. Якщо так,
# вивести повідомлення "Введений час є допустимим." 

minutu = int(input("Введіть годину время: "))
hour = int(input("Введіть хвилину время"))
if 0 <= minutu <= 60 and 0 <= hour <= 24 :
    print("Введений час є допустимим")
else:
    print("Введений час не є допустимим")
